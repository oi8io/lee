package problems

import "testing"

func Test_longestCommonPrefix(t *testing.T) {
	type args struct {
		strs []string
	}
	tests := []struct {
		name string
		args args
		want string
	}{
		// TODO: Add test cases.
		{"", args{[]string{"aa2caaaaaaaa", "aa2aaaaaaaaa", "aa2aaaaaaaaaaaaaaaaaaaaaaaa"}}, "aa2"},
		{"", args{[]string{"aaasfas", "xasfsdfa", "dasdsfafs"}}, ""},
		{"", args{[]string{"a", "ax"}}, "a"},
		{"", args{[]string{"flower", "flower", "flower", "flower"}}, "flower"},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := longestCommonPrefix(tt.args.strs); got != tt.want {
				t.Errorf("longestCommonPrefix() = %v, want %v", got, tt.want)
			}
		})
	}
}
